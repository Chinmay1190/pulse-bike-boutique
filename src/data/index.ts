// Generate more products to reach 65+
export const generateMoreProducts = () => {
  const additionalProducts: Product[] = [];
  
  // Generate variants of existing products
  for (let i = 1; i <= 50; i++) {
    const baseProduct = products[i % products.length];
    const variant = {
      ...baseProduct,
      id: `${baseProduct.id}-variant-${i}`,
      name: `${baseProduct.name} Special Edition ${i}`,
      price: Math.round(baseProduct.price * (0.85 + (Math.random() * 0.3))),
      inStock: Math.random() > 0.2,
      featured: Math.random() > 0.9,
      description: `Special Edition ${i}: ${baseProduct.description}`,
    };
    
    additionalProducts.push(variant);
  }
  
  return additionalProducts;
};

export const allProducts = [...products, ...generateMoreProducts()].slice(0, 70);

// Helper function to format price in Indian Rupees
export const formatPrice = (price: number): string => {
  const formatter = new Intl.NumberFormat('en-IN', {
    style: 'currency',
    currency: 'INR',
    maximumFractionDigits: 0,
  });
  
  return formatter.format(price);
};
